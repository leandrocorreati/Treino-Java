
package br.com.projeto03;

public abstract class Quadrilateros 
{
    private float lado1;
    private float lado2;
    private float lado3;
    private float lado4;
    
    public void Quadrilateros(float lado1, float lado2, float lado3, float lado4)
    {
        this.lado1 = lado1;
        this.lado2 = lado2;
        this.lado3 = lado3;
        this.lado4 = lado4;
                
    }
    
}
